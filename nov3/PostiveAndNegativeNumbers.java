package com.digiair.training.nov3;

public class PostiveAndNegativeNumbers 
{
	public static void main(String[] args) 
	{
	double a = 0;//Declaration and instantiation
	
//using if, else if condition to find out whether the number is >0, <0 or =0)	
	if (a>0)
		System.out.println("The given number "+a+ " is a positive number");
	else if (a<0)
		System.out.println("The given number "+a+ " is a negative number");
	else
		System.out.println("The given number "+a+ " is a neutral number");
	
	
	}

}
