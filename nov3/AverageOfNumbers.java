package com.digiair.training.nov3;

public class AverageOfNumbers {

	public static void main(String[] args) {
		int a=10; //Declaration and instantiation
		float b=29.8f; //Declaration and instantiation
		int c=25;//Declaration and instantiation
		float sum=a+b+c; //Declaration and instantiation
		float avg=sum/3; //Declaration and instantiation
		System.out.println("The average of 3 Numbers: "+a+", "+b+", "+c+" is"+" "+avg);//Printing the output
				
	}

}
