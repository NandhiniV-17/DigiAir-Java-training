package com.digiair.training.nov3;

public class MathematicalOperations {
	public static void main(String[] args) 
	{
		double a = 100.4;
		int b = 4;
		double sum, sub, mul, quo, rem;
		sum = a + b;
		sub = a - b;
		mul = a*b;
		quo = a/b;
		rem = a%b;
		System.out.println("The sum of "+a+" and "+b+" is "+sum);
		System.out.println("The difference between "+a+" and "+b+" is "+sub);
		System.out.println("The multiplication of "+a+" and "+b+" is "+mul);
		System.out.println("The quotient of "+a+" and "+b+" is "+quo);
		System.out.println("The reminder of "+a+" and "+b+" is "+rem);
		
	}
	
}
